    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try
        {
          if(!jtfRut.getText().equals(""))
          {  
            persona=new Persona(jtfRut.getText(),jtfNombre.getText(),
                                 jtfFono.getText(),jtfFoto.getText());
            try {
                PersonaDB.agregar(persona);
                limpiar();
            } catch (Exception ev) {
                //Logger.getLogger(IngresoView.class.getName()).log(Level.SEVERE, null, ex);
                if(ev.getMessage().equals("Error_RUT"))
               JOptionPane.showMessageDialog(null, "ERROR  con el formato del rut...");
                
            }
          }
          else
              JOptionPane.showMessageDialog(null, "Registro en blanco...");
        }
        catch(Exception ev)
        {
           
        }
    }//GEN-LAST:event_jButton1ActionPerformed


