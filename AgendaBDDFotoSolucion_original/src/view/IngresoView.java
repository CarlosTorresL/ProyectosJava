    private void jtfRutKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtfRutKeyTyped
        // TODO add your handling code here:
        //System.out.println(evt.getKeyChar());
        if( !(evt.getKeyChar()>=48 && evt.getKeyChar()<=57 || evt.getKeyChar()=='-' || 
                evt.getKeyChar()=='k'  ||  evt.getKeyChar()=='K'))
        {
           evt.consume();
        }   
        if(jtfRut.getText().length()>=10)
            evt.consume();
    }//GEN-LAST:event_jtfRutKeyTyped

